window._onFreqChange = function () {
    const freq = document.getElementById('routine-frequency')?.value;
    const wrapper = document.getElementById('specific-days-wrapper');
    if (wrapper) wrapper.style.display = freq === 'specific_days' ? 'block' : 'none';
};

window._openCreateRoutine = function () {
    if (!canPerformAction(_profile, 'edit_task')) {
        toast.warning('Sem permissão', 'Você precisa ser editor ou admin.');
        return;
    }
    if (!document.getElementById('modal-routine')) createRoutineModal();
    _editingId = null;
    document.getElementById('form-routine')?.reset();
    document.getElementById('specific-days-wrapper').style.display = 'none';
    const titleEl = document.querySelector('#modal-routine .modal-title');
    if (titleEl) titleEl.textContent = 'Nova Rotina';
    openModal('modal-routine');
    window.renderIcons?.();
};
// Ensure modal exists before any DOM access
if (!document.getElementById('modal-routine')) createRoutineModal();

window._editRoutine = function (id) {
    const routine = _routines.find(r => r.id === id);
    if (!routine) return;
    _editingId = id;

    setTimeout(() => {
        document.getElementById('routine-title').value = routine.title || '';
        document.getElementById('routine-objective').value = routine.objective || '';
        document.getElementById('routine-description').value = routine.description || '';
        document.getElementById('routine-frequency').value = routine.frequency || 'daily';
        document.getElementById('routine-gut-g').value = routine.gut?.gravity || 1;
        document.getElementById('routine-gut-u').value = routine.gut?.urgency || 1;
        document.getElementById('routine-gut-t').value = routine.gut?.trend || 1;

        const assigneeEl = document.getElementById('routine-assignee');
        if (assigneeEl && routine.assigneeId) {
            assigneeEl.value = routine.assigneeId;
            renderAssigneeContainer(routine.assigneeId, routine.assigneeName || '');
        }

        // Specific days
        document.querySelectorAll('[name="specific-day"]').forEach(cb => {
            cb.checked = (routine.specificDays || []).includes(parseInt(cb.value));
        });
        window._onFreqChange();

        document.querySelector('#modal-routine .modal-title').textContent = 'Editar Rotina';
    }, 50);

    openModal('modal-routine');
    window.renderIcons?.();
};

window._toggleRoutine = async function (id, currentlyActive) {
    const action = currentlyActive ? 'desativar' : 'ativar';
    const confirmed = await confirmDialog(
        `${currentlyActive ? 'Desativar' : 'Ativar'} Rotina`,
        `Deseja ${action} esta rotina?`,
        currentlyActive ? 'Desativar' : 'Ativar',
        currentlyActive
    );
    if (!confirmed) return;
    try {
        await updateDoc(doc(db, 'routines', id), {
            active: !currentlyActive,
            updatedAt: serverTimestamp()
        });
        toast.success('Atualizado', `Rotina ${!currentlyActive ? 'ativada' : 'desativada'}.`);
        await loadRoutines();
        renderRoutines();
    } catch (e) {
        toast.error('Erro', 'Não foi possível atualizar a rotina.');
    }
};

