// SALVAR ROTINA
// ================================================================
window._saveRoutine = async function () {
    const title = sanitizeText(document.getElementById('routine-title')?.value?.trim() || '');
    const objective = sanitizeText(document.getElementById('routine-objective')?.value?.trim() || '');
    const description = sanitizeText(document.getElementById('routine-description')?.value?.trim() || '');
    const frequency = document.getElementById('routine-frequency')?.value || 'daily';
    const assigneeId = document.getElementById('routine-assignee')?.value || '';
    const assigneeName = getUserName(assigneeId);
    const g = parseInt(document.getElementById('routine-gut-g')?.value || 1);
    const u = parseInt(document.getElementById('routine-gut-u')?.value || 1);
    const t = parseInt(document.getElementById('routine-gut-t')?.value || 1);

    const specificDays = frequency === 'specific_days'
        ? Array.from(document.querySelectorAll('[name="specific-day"]:checked')).map(cb => parseInt(cb.value))
        : [];

    if (!title) { toast.warning('Campo obrigatório', 'Informe o título da rotina.'); return; }
    if (frequency === 'specific_days' && specificDays.length === 0) {
        toast.warning('Selecione os dias', 'Escolha pelo menos um dia da semana.'); return;
    }

    const gut = buildGutObject(g, u, t);
    const nextDue = calcNextDue(frequency, specificDays);
    const payload = {
        title, objective, description, frequency, specificDays, gut,
        assigneeId, assigneeName, active: true,
        nextDue: nextDue ? Timestamp.fromDate(nextDue) : null,
        updatedAt: serverTimestamp(),
    };

    const btn = document.getElementById('btn-save-routine');
    if (btn) btn.disabled = true;

    try {
        if (_editingId) {
            await updateDoc(doc(db, 'routines', _editingId), payload);
            toast.success('Atualizado', 'Rotina atualizada.');
        } else {
            const ndoc = await addDoc(collection(db, 'routines'), {
                ...payload, createdBy: _profile.uid, lastGenerated: null, createdAt: serverTimestamp()
            });
            await logAudit(AUDIT_ACTIONS.ROUTINE_CREATED, ndoc.id, 'routine', { title });
            toast.success('Criado', 'Rotina criada com sucesso.');
        }
        closeModal('modal-routine');
        await loadRoutines();
        renderRoutines();
    } catch (e) {
        console.error('[Routines] Save error:', e);
        toast.error('Erro', 'Não foi possível salvar a rotina.');
    } finally {
        if (btn) btn.disabled = false;
    }
};

