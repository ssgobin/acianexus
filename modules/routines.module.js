function setupListeners() {
    document.getElementById('routine-tabs')?.addEventListener('click', e => {
        const btn = e.target.closest('[data-filter]');
        if (!btn) return;
        document.querySelectorAll('#routine-tabs .filter-tab').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        renderRoutines(btn.dataset.filter);
    });
}

function maybeOpenCreateRoutineFromQuery() {
    const params = new URLSearchParams(window.location.search);
    if (params.get('create') !== '1') return;
    if (!canPerformAction(_profile, 'edit_task')) return;
    window._openCreateRoutine();
}

