async function loadUsers() {
    try {
        const snap = await getDocs(collection(db, 'users'));
        const usersById = new Map();

        snap.docs.forEach((d) => {
            const data = d.data() || {};
            const id = data.uid || d.id;
            const email = String(data.email || '').trim().toLowerCase();
            const fallbackName = email ? email.split('@')[0] : '';
            const name = String(data.name || data.displayName || data.fullName || fallbackName).trim();

            if (data.active === false) return;
            if (!name && !email) return;

            usersById.set(id, {
                id,
                name: name || email,
                email,
                active: true,
            });
        });

        if (_profile?.uid && !usersById.has(_profile.uid)) {
            usersById.set(_profile.uid, {
                id: _profile.uid,
                name: _profile.name || _profile.email || 'Você',
                email: (_profile.email || '').toLowerCase(),
                active: true,
            });
        }

        _allUsers = Array.from(usersById.values())
            .sort((a, b) => (a.name || a.email || '').localeCompare(b.name || b.email || '', 'pt-BR'));
    } catch (e) {
        _allUsers = _profile?.uid ? [{
            id: _profile.uid,
            name: _profile.name || _profile.email || 'Você',
            email: (_profile.email || '').toLowerCase(),
            active: true,
        }] : [];
    }
}

async function loadRoutines() {
    try {
        const snap = await getDocs(query(collection(db, 'routines'), orderBy('createdAt', 'desc')));
        _routines = snap.docs.map(d => ({ id: d.id, ...d.data() }));
    } catch (e) {
        console.error('[Routines] Load error:', e);
        _routines = [];
    }
}

// ================================================================
// AUTO-GERAR TAREFAS VENCIDAS
// ================================================================
async function autoGenerateDueTasks() {
    const active = _routines.filter(r => r.active !== false);
    let generated = 0;
    for (const routine of active) {
        if (!shouldGenerateRoutine(routine)) continue;
        try {
            // Verificar se já foi gerada hoje para esta rotina
            const today = todayString();
            const existing = await getDocs(query(
                collection(db, 'tasks'),
                where('routineId', '==', routine.id),
                where('routineDate', '==', today)
            ));
            if (!existing.empty) continue;

            const nextDue = calcNextDue(routine.frequency, routine.specificDays);
            await addDoc(collection(db, 'tasks'), {
                title: routine.title,
                board: 'routines',
                status: 'todo',
                assigneeId: routine.assigneeId || '',
                assigneeName: routine.assigneeName || '',
                objective: routine.objective || '',
                description: routine.description || '',
                additionalInfo: '',
                gut: routine.gut || { gravity: 1, urgency: 1, trend: 1, score: 1 },
                checklist: routine.checklist || [],
                parentTaskId: null,
                routineId: routine.id,
                routineDate: today,
                dueDate: null,
                createdBy: 'system',
                requesterId: 'system',
                requesterName: 'Sistema (Rotina)',
                createdAt: serverTimestamp(),
                updatedAt: serverTimestamp(),
            });

            // Atualiza lastGenerated e nextDue
            await updateDoc(doc(db, 'routines', routine.id), {
                lastGenerated: serverTimestamp(),
                nextDue: nextDue ? Timestamp.fromDate(nextDue) : null,
                updatedAt: serverTimestamp(),
            });
            generated++;
        } catch (e) {
            console.error('[Routines] Auto-generate error for', routine.id, e);
        }
    }
    if (generated > 0) {
        toast.info('Rotinas', `${generated} tarefa(s) gerada(s) automaticamente.`);
        await loadRoutines(); // recarregar após geração
    }
}

