// ================================================================
// INIT
// ================================================================
async function init() {
    setTheme(getStoredTheme());

    _profile = await requireAuth('viewer');
    if (!_profile) return;

    renderSidebar('routines', _profile);
    renderHeader(_profile, 'Rotinas', 'Tarefas recorrentes automáticas');

    document.getElementById('page-content').innerHTML = buildLayout();

    await Promise.all([loadUsers(), loadRoutines()]);
    await autoGenerateDueTasks();
    renderRoutines();
    setupListeners();
    createRoutineModal();
    maybeOpenCreateRoutineFromQuery();

    document.getElementById('app-loading').style.display = 'none';
    document.getElementById('app').style.display = 'flex';
    window.renderIcons?.();
}

