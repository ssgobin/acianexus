function renderRoutines(filter = 'active') {
    const container = document.getElementById('routines-container');
    if (!container) return;

    let filtered = _routines;
    if (filter === 'active') filtered = _routines.filter(r => r.active !== false);
    if (filter === 'inactive') filtered = _routines.filter(r => r.active === false);

    if (filtered.length === 0) {
        container.innerHTML = `
      <div class="empty-state">
        <div class="empty-state-icon"><i data-fa-icon="repeat"></i></div>
        <h3>Nenhuma rotina encontrada</h3>
        <p>Crie rotinas para automatizar tarefas recorrentes.</p>
        ${canPerformAction(_profile, 'edit_task') ? `<button class="btn btn-primary" onclick="window._openCreateRoutine()">
          <i data-fa-icon="plus"></i> Nova Rotina
        </button>` : ''}
      </div>`;
        window.renderIcons?.();
        return;
    }

    container.innerHTML = `
    <div class="table-wrapper">
      <table class="table">
        <thead>
          <tr>
            <th>Rotina</th>
            <th>Frequência</th>
            <th>Responsável</th>
            <th>Prioridade</th>
            <th>Próxima Geração</th>
            <th>Status</th>
            <th style="width:80px"></th>
          </tr>
        </thead>
        <tbody id="routines-tbody">
          ${filtered.map(r => buildRoutineRow(r)).join('')}
        </tbody>
      </table>
    </div>`;
    window.renderIcons?.();
}

function buildRoutineRow(r) {
    const next = r.nextDue ? formatDate(r.nextDue, 'short') : '—';
    const gutBadge = renderGutBadge(r.gut);
    const freqLabel = FREQUENCY_LABELS[r.frequency] || r.frequency || '—';
    const daysLabel = r.frequency === 'specific_days' && r.specificDays?.length
        ? ` (${r.specificDays.map(d => DAY_NAMES[d]).join(', ')})`
        : '';
    const active = r.active !== false;
    const canEdit = canPerformAction(_profile, 'edit_task');

    return `
    <tr data-id="${r.id}">
      <td>
        <div style="font-weight:600;color:var(--text-primary)">${escapeHtml(r.title)}</div>
        ${r.objective ? `<div style="font-size:11px;color:var(--text-muted)">${escapeHtml(r.objective)}</div>` : ''}
      </td>
      <td style="font-size:13px">${freqLabel}${daysLabel}</td>
      <td>
        ${r.assigneeName ? `
          <div style="display:flex;align-items:center;gap:8px">
            <div class="assignee-avatar-sm" style="background:${stringToColor(r.assigneeName)}">${getInitials(r.assigneeName)}</div>
            <span style="font-size:13px">${escapeHtml(r.assigneeName)}</span>
          </div>` : '<span style="color:var(--text-muted)">—</span>'}
      </td>
      <td>${gutBadge}</td>
      <td style="font-size:13px">${next}</td>
      <td>
        <span class="status-chip ${active ? 'status-in_progress' : 'status-cancelled'}">
          <span class="status-dot"></span>${active ? 'Ativa' : 'Inativa'}
        </span>
      </td>
      <td>
        ${canEdit ? `
          <div style="display:flex;gap:4px">
            <button class="btn btn-ghost btn-xs" title="Editar" onclick="window._editRoutine('${r.id}')">
              <i data-fa-icon="edit-2" style="width:13px;height:13px"></i>
            </button>
            <button class="btn btn-ghost btn-xs danger" title="${active ? 'Desativar' : 'Ativar'}"
                    onclick="window._toggleRoutine('${r.id}', ${active})">
              <i data-fa-icon="${active ? 'pause-circle' : 'play-circle'}" style="width:13px;height:13px"></i>
            </button>
          </div>` : ''}
      </td>
    </tr>`;
}

